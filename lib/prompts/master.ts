import { STAGE_TITLES } from "@/lib/form-fields";
export { buildStageSystemPrompt } from "@/lib/prompts/build-stage-prompt";

export function stageInstruction(stage: number, isContinue: boolean): string {
  const title = STAGE_TITLES[stage] ?? `Этап ${stage}`;

  if (isContinue && stage > 0) {
    return [
      `Пользователь подтвердил переход («Продолжить»).`,
      `Сначала выполни блок «Контроль перехода» по предыдущему этапу.`,
      `Затем выполни только ЭТАП ${stage}: ${title}.`,
      `Следуй обязательному формату ответа из системного промпта (13 пунктов + статус GREEN/YELLOW/RED/BLACK).`,
      `Не переходи к этапу ${stage + 1}. Заверши вопросом «Перейти к следующему этапу?»`,
    ].join(" ");
  }

  if (stage === 0) {
    return [
      `Начни с ЭТАПА 0: ${title}.`,
      `Используй данные проекта ниже.`,
      `Если критичных пробелов мало — выполни этап 0 полностью по формату из системного промпта.`,
      `Не переходи к этапу 1 без вопроса «Перейти к следующему этапу?»`,
    ].join(" ");
  }

  return [
    `Выполни ЭТАП ${stage}: ${title}.`,
    `Следуй обязательному формату ответа из системного промпта.`,
    `Не переходи дальше автоматически.`,
  ].join(" ");
}

export function stageReplyInstruction(stage: number): string {
  const title = STAGE_TITLES[stage] ?? `Этап ${stage}`;

  return [
    `[Инструкция для модели — не цитируй дословно]`,
    `Пользователь дополнил или исправил данные в рамках ЭТАПА ${stage}: ${title}.`,
    `Не перезапускай все 13 пунктов этапа.`,
    `Обнови только затронутые блоки: жёсткий вывод, таблицы, «Какие данные ещё нужны», go/no-go, статус GREEN/YELLOW/RED/BLACK.`,
    `Явно укажи, что изменилось после уточнения пользователя.`,
    `Сохрани нетронутые части анализа без повторения.`,
    `Заверши вопросом: «Есть ещё уточнения или перейти к следующему этапу?»`,
  ].join(" ");
}
